declare(strict_types=1);

namespace Aheadworks\Ca\Model\Service;

use Magento\Framework\ObjectManagerInterface;
use Aheadworks\Ca\Model\HistoryLog\ManagementInterface;

/**
 * HistoryLogService Factory
 */
class HistoryLogServiceFactory
{
    /**
     * @var ObjectManagerInterface
     */
    private $objectManager;

    /**
     * Constructor
     *
     * @param ObjectManagerInterface $objectManager
     */
    public function __construct(
        ObjectManagerInterface $objectManager
    ) {
        $this->objectManager = $objectManager;
    }

    /**
     * Create new History Log Service
     *
     * @return ManagementInterface
     */
    public function create(): ManagementInterface
    {
        return $this->objectManager->get(HistoryLogService::class);
    }
}
